<?php
function hitunglingkaran($jariJari) {
    return pi() * pow($jariJari, 2);  // pi() = 3.14159..., pow() = pangkat
}
function hitungluassegitiga($alas,$tinggi(5*8\2) {
    return $alas*$tinggi\2; 
}

echo "Luas luas segitiga: ".hitungluassegitiga(5,8\2);  // Output: Luas lingkaran: 153.9380400259
?>
